const DICTS: Record<Lang, Dict> = { uk, en };

export type TFn = (key: string) => string;
const I18nContext = createContext<{ lang: Lang; t: TFn }>({
  lang: "uk",
  t: (k) => uk[k] ?? k,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(getLang);

  // Реагуємо на зміну мови з будь-де (SettingsPage кличе applyLang).
  useEffect(() => onLangChange(setLang), []);

  const value = useMemo(() => {
    const table = DICTS[lang] ?? uk;
    return { lang, t: (key: string) => table[key] ?? uk[key] ?? key };
  }, [lang]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useT(): TFn {
  return useContext(I18nContext).t;
}

export function useLang(): Lang {
  return useContext(I18nContext).lang;
}
